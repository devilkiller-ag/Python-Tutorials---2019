# Mutable - can change - lists
# Immutable - cannot change - tuples
tp = (1, 2, 3)
'''If tuple contains only one element then it is printed simply as element and parentheses are not shown and therefore tuple is not formed and value is simply taken variable.
 To show parentheses an comma is placed after element. therefore tuple is formed and value acts as an tuple'''
tp1 = (1)
tp2 = (2,)
print(tp)
print(tp1)
print(tp2)
