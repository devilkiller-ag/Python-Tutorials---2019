# File IO Basics
'''
Modes in which an file can be opened:
"r" - Open file for reading - default mode            //read mode
"w" - Open file for writing                           //write mode
"x" - Creates file if not exists                      //exclusive creation mode
"a" - Add more content to a file                      //append mode
"t" - text mode - default mode
"b" - binary mode
"+" - read and write                                  //+ mode
'''
