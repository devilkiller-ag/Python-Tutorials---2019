print('Code with AG', end='') # end= is a new line sequence
print(', Ashmit Gupta')
print('AG')
print('C:\Ashmit')
print('C:\nAshmit') # \n is an escape sequence
print('C:\\nAshmit')
print('C:''AG')# '' is not printed
print('C:\'\'AG') # '' is printed
print('C:\'AG') # ' is printed
# escape sequence chaecters
print('Ashmit\' Gupta \n is a good boy \t AG \t')